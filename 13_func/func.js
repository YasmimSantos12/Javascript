function somar(n1=10,n2=3){
    console.log(`A soma de ${n1} + ${n2} = ${n1+n2}`)
}
function sub(n1=10,n2=3){
    console.log(`A subtração de ${n1} - ${n2} = ${n1-n2}`)
}
function mult(n1=10,n2=3){
    console.log(`A multiplicação de ${n1} * ${n2} = ${n1*n2}`)
}
function div(n1=10,n2=3){
    console.log(`A divisão de ${n1} / ${n2} = ${parseInt(n1/n2)}`)
}



